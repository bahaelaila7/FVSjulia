    local dbhcm::Float32 = Float32(dbh) * 2.54f0
    local spg::Int = 1
    local temp::Int = 2
    local mois::Int = 2
    local sml::Int  = 2

    if VARACD == "EC"
        temp = Int(ECHMC[ITYPE]); mois = Int(ECWMD[ITYPE])
        spg  = Int(ECSPEC[ksp])
        d1 = ECDBH1[ksp]; d2 = _dbh2(d1)
        sml = dbhcm < d1 ? 1 : (dbhcm < d2 ? 2 : 3)
    elseif VARACD == "BM"
        temp = Int(BMHMC[ITYPE]); mois = Int(BMWMD[ITYPE])
        spg  = Int(BMSPEC[ksp])
        d1 = BMDBH1[ksp]; d2 = _dbh2(d1)
        sml = dbhcm < d1 ? 1 : (dbhcm < d2 ? 2 : 3)
    elseif VARACD == "SO"
        temp = Int(SOHMC[ITYPE]); mois = Int(SOWMD[ITYPE])
        spg  = Int(SOSPEC[ksp])
        d1 = SODBH1[ksp]; d2 = _dbh2(d1)
        sml = dbhcm < d1 ? 1 : (dbhcm < d2 ? 2 : 3)
    elseif VARACD == "PN" || VARACD == "OP"
        temp = Int(PNWMC[ITYPE]); mois = Int(PNWMD[ITYPE])
        spg  = Int(WSSPEC[ksp])
        d1 = WSDBH1[ksp]; d2 = _dbh2(d1)
        sml = dbhcm < d1 ? 1 : (dbhcm < d2 ? 2 : 3)
    elseif VARACD == "WC"
        temp = Int(WCWMC[ITYPE]); mois = Int(WCWMD[ITYPE])
        spg  = Int(WSSPEC[ksp])
        d1 = WSDBH1[ksp]; d2 = _dbh2(d1)
        sml = dbhcm < d1 ? 1 : (dbhcm < d2 ? 2 : 3)
    elseif VARACD == "AK"
        temp = 3; mois = 1   # cold, wet
        spg  = Int(AKSPEC[ksp])
        d1 = AKDBH1[ksp]; d2 = _dbh2(d1)
        sml = dbhcm < d1 ? 1 : (dbhcm < d2 ? 2 : 3)
    else
        # SN and all other variants — never actually reached; FMSNGDK uses linear DBH formula
        spg = 1; temp = 2; mois = 2; sml = 2
    end

    # Look up years-to-soft and fall adjustment
    local xval::Int32
    local yval::Int32
    if VARACD == "PN" || VARACD == "AK" || VARACD == "OP"
        xval = PNYRSOFT[spg, temp, mois, sml]
        yval = PNDCYADJ[spg, temp, mois, sml]
    elseif VARACD == "WC"
        xval = WCYRSOFT[spg, temp, mois, sml]
        yval = WCDCYADJ[spg, temp, mois, sml]
    else  # EC, BM, SO, and DEFAULT
        xval = ESYRSOFT[spg, temp, mois, sml]
        yval = ESDCYADJ[spg, temp, mois, sml]
    end

    if debug
        @printf(get(io_units, Int32(JOSTND), stdout),
            " FMR6SDCY X=%5d Y=%5d SPG=%3d TEMP=%2d MOIS=%2d SML=%2d DBHCM=%6.2f\n",
            xval, yval, spg, temp, mois, sml, dbhcm)
    end

    x_ref[]   = xval
    y_ref[]   = yval
    sml_ref[] = Int32(sml)
    return nothing
end
