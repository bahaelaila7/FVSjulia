# fire/fmphotoval.f — Photo series fuel load tables (32 photo series)
# FMPHOTOVAL: looks up VLH (hard wood, 11 size classes) and VLS (soft wood, 9 classes)
# from one of 32 calibrated photo series tables; dispatches by FOTOREF (1-32).

# Compute (vlh, vls) from VLT + PROPROT vector.
# Rows 1-3: VLH=VLT, VLS=0
# Rows 4-9: VLS=VLT*PR[j], VLH=max(VLT-VLS, 0)
# Rows 10-11 (litter, duff): VLH=VLT (via copy), no VLS
function _mk_proprot(vlt::Matrix{Float32}, pr::Vector{Float32})
    n   = size(vlt, 2)
    vlh = copy(vlt)
    vls = zeros(Float32, 9, n)
    for j in 1:n
        for i in 1:3
            vlh[i, j] = vlt[i, j]
        end
        for i in 4:9
            vls[i, j] = vlt[i, j] * pr[j]
            vlh[i, j] = max(vlt[i, j] - vls[i, j], 0f0)
        end
        # rows 10-11: vlh already = vlt from copy(); no vls entry
    end
    (vlh, vls)
end

