        fotocode > 30 && @goto label_50
        fotoval[1:MXFLCL] .= _REF28[1][:, fotocode]
        fotovals[1:9]     .= _REF28[2][:, fotocode]
    elseif fotoref == 29
        fotocode > 16 && @goto label_50
        fotoval[1:MXFLCL] .= _REF29[1][:, fotocode]
        fotovals[1:9]     .= _REF29[2][:, fotocode]
    elseif fotoref == 30
        fotocode > 16 && @goto label_50
        fotoval[1:MXFLCL] .= _REF30[1][:, fotocode]
        fotovals[1:9]     .= _REF30[2][:, fotocode]
    elseif fotoref == 31
        fotocode > 10 && @goto label_50
        fotoval[1:MXFLCL] .= _REF31[1][:, fotocode]
        fotovals[1:9]     .= _REF31[2][:, fotocode]
    elseif fotoref == 32
        fotocode > 39 && @goto label_50
        fotoval[1:MXFLCL] .= _REF32[1][:, fotocode]
        fotovals[1:9]     .= _REF32[2][:, fotocode]
    end

    @label label_50

    if DEBUG
        @printf(io_units[JOSTND], " IN FMPHOTOVAL, FOTOREF=%4d FOTOCODE=%4d\n",
                fotoref, fotocode)
        @printf(io_units[JOSTND], " %s\n",
                join([@sprintf("%10.4f", fotoval[i]) for i in 1:11]))
        @printf(io_units[JOSTND], " %s\n",
                join([@sprintf("%10.4f", fotovals[i]) for i in 1:9]))
    end

    if fotoval[1] < 0f0
        @printf(io_units[JOSTND],
            "\n *** FFE MODEL WARNING: INCORRECT PHOTO REFERENCE OR PHOTO CODE ENTERED.\n \n")
        RCDSET(Int32(2), true)
    end

    return
end
