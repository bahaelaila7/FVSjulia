function BRATIO(is::Integer, d::Real, h::Real)::Float32
    # h unused; suppress warning
    _rdanuw = Float32(h)
    df = Float32(d)
    if df > Float32(0)
        dib = if IFOR == Int32(20)
            if is == 5
                Float32(0.1713)   + Float32(0.87459) * df
            elseif is == 6
                Float32(-0.26207) + Float32(0.87347) * df
            elseif is == 8
                Float32(-0.43439) + Float32(0.91382) * df
            elseif is == 11
                Float32(-0.62033) + Float32(0.91645) * df
            elseif is == 13
                Float32(-0.4671)  + Float32(0.90198) * df
            else
                _BRATIO_BARKC[1, is] + _BRATIO_BARKC[2, is] * df
            end
        else
            _BRATIO_BARKC[1, is] + _BRATIO_BARKC[2, is] * df
        end
        ratio = dib / df
    else
        ratio = Float32(0.99)
    end
    if ratio > Float32(0.99); ratio = Float32(0.99); end
    if ratio < Float32(0.80); ratio = Float32(0.80); end
    return ratio
end
