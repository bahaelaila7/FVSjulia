"""
    HTCALC(n, ispc, aget, h, htmax, htg1, jostnd, debug)

NC128 height curve for species `ispc`.
- `n ≤ 0`:   solve for `aget` from current height `h` (modified in-place via Ref)
- `0 < n < 2`: solve for `h` from `aget` (modified in-place via Ref)
- `n ≥ 2`:   compute 5-year height increment `htg1` (modified in-place via Ref)
`htmax` is always returned.
"""
function HTCALC(n::Int32, ispc::Int32,
                aget::Ref{Float32}, h::Ref{Float32},
                htmax::Ref{Float32}, htg1::Ref{Float32},
                jostnd::Int32, debug::Bool)
    if debug
        @printf(io_units[jostnd], " ENTERING HTCALC, MODE=  %d\n", n)
    end

    b1 = HTCALC_LTBHEC[(ispc-1)*5+1]
    b2 = HTCALC_LTBHEC[(ispc-1)*5+2]
    b3 = HTCALC_LTBHEC[(ispc-1)*5+3]
    b4 = HTCALC_LTBHEC[(ispc-1)*5+4]
    b5 = HTCALC_LTBHEC[(ispc-1)*5+5]
    si = SITEAR[ispc]

    # Non-mountain yellow poplar (ISPC==45, PCOM not starting with 'M')
    if ispc == Int32(45) && (isempty(PCOM) || PCOM[1] != 'M')
        b1 = Float32(1.1789)
        b2 = Float32(1.0)
        b3 = Float32(-0.0339)
        b4 = Float32(0.8117)
        b5 = Float32(-0.0001)
    end

    if debug
        @printf(io_units[jostnd], " HTCALC B1,B2,B3,B4,B5,SI= %f %f %f %f %f %f\n",
                b1, b2, b3, b4, b5, si)
    end

    hb = Float32(0.0)
    htmax[] = b1 * si^b2

    if debug
        @printf(io_units[jostnd], " HTCALC HTMAX,H =%f %f\n", htmax[], h[])
    end

    if htmax[] - h[] <= Float32(1.0)
        @goto label_900
    end

    if n <= Int32(0)
        # solve for age from height
        ratio = (h[] - hb) / (b1 * si^b2)
        if ratio < Float32(0.0); ratio = Float32(0.0); end
        if ratio > Float32(1.0); ratio = Float32(1.0) - Float32(1e-6); end
        aget[] = Float32(1.0) / b3 * log(Float32(1.0) - ratio^(Float32(1.0)/(b4*si^b5)))
        if debug
            @printf(io_units[jostnd], " HTCALC H,HB,AGET= %f %f %f\n", h[], hb, aget[])
        end
        @goto label_900
    end

    if n > Int32(0) && n < Int32(2)
        # solve for height from age
        h[] = hb + b1 * si^b2 * (Float32(1.0) - exp(b3 * aget[]))^(b4 * si^b5)
        if debug
            @printf(io_units[jostnd], " HTCALC HB,AGET,H= %f %f %f\n", hb, aget[], h[])
        end
        @goto label_900
    end

    # n ≥ 2: compute 5-year increment
    let
        htg0  = hb + b1 * si^b2 * (Float32(1.0) - exp(b3 * aget[]))^(b4 * si^b5)
        htgp5 = hb + b1 * si^b2 * (Float32(1.0) - exp(b3 * (aget[] + Float32(5.0))))^(b4 * si^b5)
        htg1[] = htgp5 - htg0
        if debug
            @printf(io_units[jostnd],
                " HTCALC HB,AGET,HTG0,HTGP5,HTG1= %f %f %f %f %f\n",
                hb, aget[], htg0, htgp5, htg1[])
        end
    end

    @label label_900
    return nothing
end
