function NBOLT(ispc::Integer, h::Real, d::Real,
               dbhmin::AbstractVector{Float32}, bfmind::AbstractVector{Float32},
               sindx::Real, topdob::Real, bftdob::Real,
               jostnd_arg::Integer, debug::Bool,
               iht1_ref::Ref{Int32}, iht2_ref::Ref{Int32})

    io = io_units[Int32(jostnd_arg)]
    if debug; @printf(io, " ENTERING NBOLT \n"); end

    isp = Int(ispc)
    # Select R8 vs R9 coefficients
    if KODFOR < 900
        c1=_NBOLT_B1[isp]; c2=_NBOLT_B2[isp]; c3=_NBOLT_B3[isp]
        c4=_NBOLT_B4[isp]; c5=_NBOLT_B5[isp]; c6=_NBOLT_B6[isp]
    else
        c1=_NBOLT_CSB1[isp]; c2=_NBOLT_CSB2[isp]; c3=_NBOLT_CSB3[isp]
        c4=_NBOLT_CSB4[isp]; c5=_NBOLT_CSB5[isp]; c6=_NBOLT_CSB6[isp]
    end

    d_f = Float32(d); h_f = Float32(h); sindx_f = Float32(sindx)
    ba_f = Float32(BA)

    if d_f < dbhmin[isp]
        iht1_ref[] = Int32(0); iht2_ref[] = Int32(0)
        return nothing
    end

    # Estimated total height (FACTOR=0)
    esttht = Float32(4.5) + c1 * (Float32(1) - exp(-c2 * d_f))^c3 *
             sindx_f^c4 * Float32(1.00001)^c5 * ba_f^c6

    if debug
        @printf(io, " ESTTHT=%f ISPC=%d C1=%f C2=%f C3=%f C4=%f C5=%f C6=%f D=%f H=%f SINDX=%f TOP/D=%f BA=%f TOPDOB=%f\n",
            esttht, isp, c1, c2, c3, c4, c5, c6, d_f, h_f, sindx_f, Float32(topdob)/d_f, ba_f, Float32(topdob))
    end

    # Pulpwood top (BFTDOB)
    factor = Float32(topdob) / d_f
    if factor > 1; factor = Float32(1); end
    estcht = Float32(4.5) + c1 * (Float32(1) - exp(-c2 * d_f))^c3 *
             sindx_f^c4 * (Float32(1.00001) - factor)^c5 * ba_f^c6
    if debug
        @printf(io, " ESTCHT=%f\n", estcht)
    end
    pulbol = estcht * (h_f / esttht)
    iht2_ref[] = Int32(floor(pulbol / Float32(8.333333)))
    if debug; @printf(io, " No. PULPWOOD BOLTS (IHT2)= %d\n", iht2_ref[]); end

    # Sawtimber top
    if d_f < bfmind[isp]
        iht1_ref[] = Int32(0)
        return nothing
    end
    factor = Float32(bftdob) / d_f
    if factor > 1; factor = Float32(1); end
    estsht = Float32(4.5) + c1 * (Float32(1) - exp(-c2 * d_f))^c3 *
             sindx_f^c4 * (Float32(1.00001) - factor)^c5 * ba_f^c6
    if debug
        @printf(io, " ESTSHT=%f\n", estsht)
    end
    sawbol = estsht * (h_f / esttht)
    iht1_ref[] = Int32(floor(sawbol / Float32(8.333333)))
    if debug; @printf(io, " No. SAWWOOD BOLTS (IHT1)= %d\n", iht1_ref[]); end

    return nothing
end
