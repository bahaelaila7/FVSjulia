function DGBND(ispc::Integer, dbh_val::Real, ddg_in::Real)::Float32
    i = Int(ispc)
    dlo = _DGBND_DLODHI[2*i - 1]
    dhi = _DGBND_DLODHI[2*i]
    ddg = Float32(ddg_in)
    d   = Float32(dbh_val)

    if d <= dlo
        # No adjustment
    elseif d > dhi
        ddg = Float32(0.048)
    else
        ddg = ddg * (Float32(1) + Float32(0.90) * ((d - dlo) / (dlo - dhi)))
        if ddg < Float32(0.048); ddg = Float32(0.048); end
    end

    # Size cap check
    if (d + ddg) > SIZCAP[i, 1] && SIZCAP[i, 3] < Float32(1.5)
        ddg = SIZCAP[i, 1] - d
        if ddg < Float32(0.01); ddg = Float32(0.01); end
    end

    return ddg
end

function DGBND(ispc::Integer, dbh_val::Real, ddg_ref::Ref{Float32})
    ddg_ref[] = DGBND(ispc, dbh_val, ddg_ref[])
    return nothing
end
