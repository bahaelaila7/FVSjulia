# formclas.jl — Region-specific form class lookup subroutines
# Translated from: formclas.f (887 lines)
#
# Used by r6vol.f to determine default form class for Pacific Northwest/CA volume equations.
# Not called by the sn variant (which uses FRMCLS array via sn/formcl.jl), but compiled in FVSsn.
#
# All subroutines share the same pattern:
#   1. Binary search for species code in local FIAJSP table
#   2. Compute DBH class (1-5)
#   3. Return forest-specific form class from DATA table

# ---------------------------------------------------------------------------
# Binary search helper (shared pattern across all FORMCL_* subroutines)
# Returns 1-based index into fiajsp where spec matches, or 0 if not found.
# ---------------------------------------------------------------------------
function _formcl_bsearch(spec::AbstractString, fiajsp::Vector{String}, n::Int)
    doneflag = false; lastflag = false
    first = 1; last = n; ispc = 0
    while !doneflag
        if first == last; lastflag = true; end
        half = (last - first + 1) ÷ 2 + first
        if spec == fiajsp[half]
            ispc = half; doneflag = true
        elseif spec > fiajsp[half]
            first = half
        else
            last = half - 1
        end
        if lastflag && !doneflag; return 0; end   # not found
    end
    return ispc
end

# Compute IFCDBH from diameter (1..5)
function _ifcdbh(d::Real)
    fc = Int(trunc((d - 1.0) / 10.0 + 1.0))
    if fc < 1; fc = 1; end
    if d > 40.9; fc = 5; end
    return fc
end

