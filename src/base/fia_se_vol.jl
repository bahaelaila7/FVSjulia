# ---------------------------------------------------------------------------
# SRS_VOL: compute cubic and board-foot volumes for one tree
# vol[1]=TCU (stump to tip), vol[4]=MCU or CV4, vol[7]=top-wood, vol[10]=BF
# bfmind_ref: min DBH for board-foot merchantability (set to 9.0 pines / 11.0 hardwoods if 0)
# ---------------------------------------------------------------------------
function SRS_VOL(voleq::AbstractString, dbhob::Real, httot::Real,
                 bfmind_ref::Ref{Float32}, vol::AbstractVector{Float32},
                 errflg_ref::Ref{Int32})
    errflg_ref[] = Int32(0)
    if dbhob < 1.0f0; errflg_ref[] = Int32(3); return; end
    if httot < 4.5f0; errflg_ref[] = Int32(4); return; end

    # Parse FIA species code from chars 8-10 of the 10-char equation number
    spn = 0
    if length(voleq) >= 10
        try; spn = parse(Int, voleq[8:10]); catch; end
    end

    dbh = Float32(dbhob)
    ht  = Float32(httot)

    # Locate species in table
    idx = get(_SRS_SP_IDX, Int32(spn), 0)
    if idx <= 0
        # Unknown species: sapling fallback from Fortran
        if dbh < 5.0f0
            vol[4] = 0.045146f0 + 0.002357f0 * dbh^2 * ht
        else
            errflg_ref[] = Int32(6)
        end
        return
    end

    # Set merchantability diameter if not supplied
    bfmind = bfmind_ref[]
    if bfmind < 0.1f0
        bfmind = spn < 300 ? 9.0f0 : 11.0f0
        bfmind_ref[] = bfmind
    end

    plantation = length(voleq) >= 7 && voleq[7] == '1'

    a1 = _SRS_CUCOEF[idx, 1]; a2 = _SRS_CUCOEF[idx, 2]
    c1 = _SRS_CUCOEF[idx, 3]; c2 = _SRS_CUCOEF[idx, 4]
    d1 = _SRS_CUCOEF[idx, 5]; d2 = _SRS_CUCOEF[idx, 6]

    # CV4: cubic volume to 4-inch top
    cv4 = 0.0f0
    if dbh < 5.0f0          # sapling
        if !plantation
            cv4 = a1 + a2 * dbh^2 * ht
        else
            b1 = (spn == 111) ? 0.132056f0 : (spn == 131) ? 0.150292f0 : a1
            b2 = (spn == 111) ? 0.002113f0 : (spn == 131) ? 0.001994f0 : a2
            cv4 = b1 + b2 * dbh^2 * ht
        end
    elseif dbh < bfmind     # pole
        cv4 = c1 + c2 * dbh^2 * ht
    else                    # sawtimber
        cv4 = d1 + d2 * dbh^2 * ht
    end
    vol[4] = cv4

    # Total cubic: CV4 * TRATIO where TRATIO = E1 + E2*(1/(D-3))²
    if dbh < 5.0f0
        vol[1] = cv4                   # sapling: TF = 1
    else
        e1 = _SRS_TFCOEF[idx, 1]; e2 = _SRS_TFCOEF[idx, 2]
        tf = if plantation
            g1 = (spn == 111) ? 1.081908f0 : (spn == 131) ? 1.081556f0 : e1
            g2 = (spn == 111) ? 2.607333f0 : (spn == 131) ? 3.65992f0  : e2
            g1 + g2 * (1.0f0 / (dbh - 3.0f0))^2
        else
            e1 + e2 * (1.0f0 / (dbh - 3.0f0))^2
        end
        vol[1] = cv4 * tf
    end

    # Sawtimber: split into sawlog and top portions; compute board feet
    if dbh >= bfmind
        h1 = _SRS_BFCOEF[idx, 1]; h2 = _SRS_BFCOEF[idx, 2]
        rc = h1 + h2 * (1.0f0 / (dbh - 5.0f0))^2
        rc = clamp(rc, 0.6f0, 0.99f0)
        mcu      = cv4 * rc
        vol[7]   = cv4 - mcu          # top-wood cubic
        vol[4]   = mcu                 # sawlog cubic (overrides pole value)
        i1 = _SRS_BFCOEF[idx, 3]; i2 = _SRS_BFCOEF[idx, 4]
        rb       = i1 + i2 * (1.0f0 - 1.0f0 / dbh)
        vol[10]  = rb * cv4            # board feet
    end

    return nothing
end
