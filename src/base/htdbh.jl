# ---------------------------------------------------------------------------
# HTDBH — Height-diameter relationship (Curtis-Arney form)
#   MODE 0: d given → h_ref[] updated
#   MODE 1: h_ref[] given → d_ref[] updated (d passed as dummy 0.0)
# For mode 0 the Julia convention matches cratet.jl: HTDBH(ifor, ispc, d, href, mode)
# For mode 1 a separate form with d_ref is used (not yet called; stub via args...)
# ---------------------------------------------------------------------------
function HTDBH(ifor::Integer, ispc::Integer, d::Real, h_ref::Ref{Float32}, mode::Integer)
    debug = DBCHK(false, "HTDBH", Int32(5), ICYC)
    if debug
        @printf(io_units[Int(JOSTND)], " ENTERING SUBROUTINE HTDBH CYCLE = %4d\n", ICYC)
    end

    p2 = _HTDBH_SNALL_MAT[1, ispc]
    p3 = _HTDBH_SNALL_MAT[2, ispc]
    p4 = _HTDBH_SNALL_MAT[3, ispc]
    db = _HTDBH_SNDBAL[ispc]

    # Fort Bragg (IFOR=20) overrides for selected species
    if ifor == 20
        if     ispc == 6;  p2=Float32(110.3); p3=Float32(7.0670); p4=Float32(-1.0420)
        elseif ispc == 8;  p2=Float32(114.6); p3=Float32(4.1840); p4=Float32(-0.6940)
        elseif ispc == 11; p2=Float32(623.9); p3=Float32(4.7396); p4=Float32(-0.2763)
        elseif ispc == 13; p2=Float32(184.3); p3=Float32(4.2660); p4=Float32(-0.5496)
        end
    end

    df = Float32(d); hf = h_ref[]

    if mode == 0
        hf = Float32(0)
        if df >= Float32(3)
            hf = Float32(4.5) + p2 * exp(-p3 * df^p4)
        else
            hat3 = Float32(4.5) + p2 * exp(-p3 * Float32(3)^p4)
            hf = ((hat3 - Float32(4.51)) * (df - db) / (Float32(3) - db)) + Float32(4.51)
        end
        h_ref[] = hf
    else
        # MODE 1: H→D (set h_ref[] before calling)
        hf = h_ref[]
        hat3 = Float32(4.5) + p2 * exp(-p3 * Float32(3)^p4)
        df = if hf >= hat3
            if debug; @printf(io_units[Int(JOSTND)], " H= %g  HAT3= %g  P2= %g\n", hf, hat3, p2); end
            ratio = (log(min(hf, Float32(4.5) + p2 * Float32(0.9999)) - Float32(4.5)) - log(p2)) / (-p3)
            ratio > 0.0f0 ? exp(log(ratio) * (Float32(1)/p4)) : Float32(100.0)
            # original: exp(log((log(hf-4.5)-log(p2))/(-p3))*(1/p4)) — fails when hf≥4.5+p2
        else
            (((hf - Float32(4.51)) * (Float32(3) - db)) / (hat3 - Float32(4.51))) + db
        end
        # For mode 1, caller must capture D via a different mechanism (currently unused)
        h_ref[] = df  # repurpose h_ref to return D when mode==1
    end

    if debug
        @printf(io_units[Int(JOSTND)], " IN HTDBH-MODE ,H, D= %d %g %g\n", mode, h_ref[], d)
        @printf(io_units[Int(JOSTND)], " IN HTDBH  P2, P3, P4, DB= %g %g %g %g\n", p2, p3, p4, db)
    end
    return nothing
end
